let nome = 'quirino'
console.log(nome) // verificação
console.error(nome) // erro
console.warn(nome) // alerta

window.alert(nome) // janela ou aba de alerta
window.prompt('insira o seu nome') // campo de inserir msg
window.confirm('confirma essa ação?') // campo de confirmação - retorna true ou false

